import pandas as pd

data = {'이름' : ['KIm', 'Park', 'Lee', 'Ho'],
        '국어' : [90, 58, 88, 100],
        '영어' : [100, 60, 80, 70],
        '수학' : [55, 65, 76, 88] }


print("1,2#################################################################################################################")
df = pd.DataFrame(data)
print(df, end='\n\n')

print("3 #################################################################################################################")
name = df['이름']
print(name, end='\n\n')

print("4 #################################################################################################################")
print(df.loc[df['이름'] == 'park'])
# print(df.loc[1])


print("5 #################################################################################################################")
df.loc[df['이름'] == 'Ho', '수학'] = 90
# df.loc[3, '수학'] = 90
print(df, end='\n\n')

print("6 #################################################################################################################")
df.loc[4] = ["Oh", 100, 70, 80]
print(df, end='\n\n')

print("7 #################################################################################################################")
df2 = df.drop([2], axis=0)
print(df2, end='\n\n')


