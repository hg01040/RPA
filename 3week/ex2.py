import pandas as pd

data = {
    '과목번호' : ['C1', 'C2', 'C3', 'C4', 'C5', 'C6'],
    '과목명' : ['인공지능개론', '웃음치료', '경영학', '3D디자인', '스포츠경영', '예술의 세계'],
    '강의실' : ['R1', 'R2', 'R3', 'R4', 'R5', 'R6'],
    '시간수' : [3, 2, 3, 4, 2, 1]
}

df = pd.DataFrame(data)
print(df, end='\n\n')

#[데이터 프레임] -- iloc 
# df.iloc[행 순서, 열 순서]
# df.iloc[:, ] --> 모든 열

print("1**************************************************************************************")
print(df.iloc[2], end='\n\n') # 2번째 행 출력

print("2**************************************************************************************")
print(df.iloc[0:5], end='\n\n') # 0~4번째 행까지 출력

print("3**************************************************************************************")
print(df.iloc[[0,3,5]], end='\n\n') # 0,3,5 행 출력

print("4**************************************************************************************")
print(df.iloc[:, 3], end='\n\n') # 3열만 출력

print("5**************************************************************************************")
print(df.iloc[:, 0:3], end='\n\n') # 0~2번째 열까지 출력

print("6**************************************************************************************")
print(df.iloc[:, [1,3]], end='\n\n') # 1,3번째 열만 출력

print("7**************************************************************************************")
print(df.iloc[0:3, 1:3], end='\n\n') # 0~2행에서 1~2열만 출력

print("8**************************************************************************************")
print(df.iloc[[1,2,5], [1,3]], end='\n\n') #1,2,5행에서 1,3열만 출력